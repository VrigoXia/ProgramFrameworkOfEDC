/*************************************************
* 函数名称: void  buttonScan(void )//按键扫描函数
* 描述: 按键检测程序
* 输入: 无
* 输出: 无
* 返回值: 无
* 其他说明: 此按键检测程序没有使用到定时器，
*           将在后续的工程中改进此函数
*************************************************/
void  buttonScan(void )//按键扫描函数
{
	static unsigned char wait;
	u8 i,num;
	if(!getButtonNum())
	{
		num = getButtonNum();
		if(num)
		{
			g_Button[num] = 1;	
			wait = 0;
		}
		else
		{
			if(wait == 1000)
			{
				for(i = 0;i < 16;i++)
				{
					g_Button[i] = 0;
				}
			}
		}
		wait++;
	}
}
